public class PromitiveDataTypeExam {
    public static void main(String[] args) {
        // boolean 타입
        boolean isFun = false;
        System.out.println(isFun);

        // 문자 타입
        // char 타입: 문자 1개
        char c = 'f';

        // 정수 타입
        // int 타입
        int x = 58;

        //long 타입
        long bing = 21354682034L;

        // 실수 타입
        //float 타입
        float f = 32.124f;

        // double 타입
        double b = 21512412.124124;
    }
}
